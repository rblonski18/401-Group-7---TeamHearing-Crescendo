SHS.prototype.changeButtons = function(){
	table = document.getElementById('shs_afc');
	jQuery(table).html('');
	var materials = this.options[5][this.index[0]][this.index[1]][this.index[2]][this.index[3]];
	jQuery.each(materials, function (index, material) {
		if (materials.length < 6) {
			var height = 80/materials.length;
			var row = table.insertRow(index);
			row.style.width = '100%';
			var cell = row.insertCell(0);
			cell.style.width = '100%';
		} else if (materials.length < 14) {
			var height = 80/Math.ceil(materials.length/2);
			if (index%2 == 0) {
				var row = table.insertRow(index/2);
				row.style.width = '100%';
			} else {
				var row = table.rows[Math.floor(index/2)];
			}
			var cell = row.insertCell(index%2);
			cell.style.fontSize = 'smaller';
			cell.style.width = '50%';
		} else {
			var height = 80/Math.ceil(materials.length/3);
			if(index%3==0){
				var row = table.insertRow(index/3);
				row.style.width = '100%';
			} else {
				var row = table.rows[Math.floor(index/3)];
			}
			var cell = row.insertCell(index%3);
			cell.style.fontSize = 'smaller';
			cell.style.width = '33%';
		}
		cell.style.height = height+'%';
		var button = document.createElement('button');
		button.className = 'afc';
		button.index = index;
		button.innerHTML = material.keyword;
		button.style.height = '100%';
		button.style.width = '100%';
		jQuery(button).button();
		jQuery(cell).append(button);
	});
	this.response();
}
SHS.prototype.createOrder = function(){
	var materials = this.materials;
	order = [];
	$.each(materials,function(index,material){
		if(!Array.isArray(order[material.category])){order[material.category]=[]};
		if(!Array.isArray(order[material.category][material.group])){order[material.category][material.group]=[]};
		if(!Array.isArray(order[material.category][material.group][material.set]))
			{order[material.category][material.group][material.set]=[]};
		if(!Array.isArray(order[material.category][material.group][material.set][material.type]))
			{order[material.category][material.group][material.set][material.type]=[]};
		order[material.category][material.group][material.set][material.type].push({
			filename:material.filename,
			keyword:material.keyword,
			phrase:material.phrase
		});
	});
	this.options.push(order);
}
