SHS.prototype.info = function(info,category,group,set,types,method){
	var items = info.length/2;
	var items_per_type = items/types.length;
	var info_per_type = info.length/types.length;
	var type = 0;
	for (item=0;item<items;item++){
		var filename = info[2*item];
		if (item<items_per_type) {
			keyword = info[2*item+1];
			phrase = undefined;
		} else {
			keyword = info[(2*item+1)%info_per_type];
			phrase = info[2*item+1];
		}
		type = Math.floor(item/items_per_type);
		this.materials.push({filename:filename,keyword:keyword,phrase:phrase,
			category:category,group:group,set:set,type:type});
	}
}
