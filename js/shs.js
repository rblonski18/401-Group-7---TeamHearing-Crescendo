SHS.prototype.createStars = function(){
	that = this;

	this.stars = [];
	for (category=0;category<this.options[1].length;category++) {
		this.stars[category] = [];
		for (group=0;group<this.options[2][category].length;group++) {
			this.stars[category][group] = 0;
		}
	}
	
	// count tests
	if (false) {
		jQuery.each(this.results, function(index,result) {
			that.stars[result.categoryID][result.groupID]++;
		});
	}
	
	// calculate stars
	var score;
	var stars;
	jQuery.each(this.results, function(index,result) {
		score = result.correct/result.total;
		if (score == 1) { stars = 3 }
		else if (score > 0.8) { stars = 2 }
		else { stars = 1 };
		that.stars[result.categoryID][result.groupID] 
			= Math.max(that.stars[result.categoryID][result.groupID],stars);
	});
}
