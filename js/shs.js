function SHS() {
	this.index = [];
	this.materials = [];
	this.mode = 'afc';
	this.order = [];
	this.practice = false;
	this.score = [];
	this.trial = 0;
	
	// test video for play type
	var testVideo = document.createElement( "video" ), mp4, ogg;
	if (testVideo.canPlayType){
		// Check for MPEG-4 support
		mp4 = "" !== testVideo.canPlayType( 'video/mp4; codecs="mp4v.20.8"' );

		// Check for Ogg support
		ogg = "" !== testVideo.canPlayType( 'video/ogg; codecs="theora"' );
	}
	if (ogg){
		this.filetype = '.ogg';
	} else {
		this.filetype = '.mp4';
	}
	
	// video
	this.video = document.createElement('video');
	this.video.controls = true;
	this.video.style.position = 'absolute';
	this.video.style.height = '96%';
	this.video.style.width = '96%';
	this.video.style.left = '2%';
	this.video.style.top = '2%';
	
	//
	this.createOptions();
}

