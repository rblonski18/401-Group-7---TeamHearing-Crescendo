BEL.prototype.select = function () {
	// shuffle
	if (this.available.length == 0) {	
		this.available.sequence(this.keywords.length);
		this.available.shuffle();
	}
	
	// deal
	var index = this.available.pop();
	
	// buttons and message
	for (var a = 0; a < 4; a++) {
		var button = document.getElementById('keyword'+a); 
		button.innerHTML = this.keywords[index][a]; 
		//try { button.removeChild(button.childNodes[1]); } catch (error) { console.dir(error); }
	}
	document.getElementById('all').index = 0;
	document.getElementById('message').innerHTML = this.phrases[index];
	
	// define source and send to processor
	this.talker = this.talkers[Math.floor(this.talkers.length*Math.random())];
	if (index<9) { var sentence = '0'+String(index+1); } else { var sentence = String(index+1); }
	if (this.list<9) { var list = '0'+String(this.list+1); } else { var list = String(this.list+1); }
	this.filename = this.path+'BEL_'+this.talker+list+sentence+this.filetype;

	return this.filename;
};
